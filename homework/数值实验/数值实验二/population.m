%-------------------population prediction-----------------%
t=1988:1996;
N=[11.10,11.27,11.43,11.58,11.72,11.85,11.99,12.11,12.24];
y=log(N);
%------------------------Least Square---------------------%
a=length(t);
b=sum(t);
c=sum(t.^2);


d=sum(y);

e=sum(t.*y);

A=eye(2);
A(1,1)=a;
A(1,2)=b;
A(2,1)=b;
A(2,2)=c;

f=zeros(2,1);
f(1,1)=d;
f(2,1)=e;

x=A\f;
%----------------plot--------------------%
y_1=zeros(1,length(t));
for i = 1:length(t)
    y_1(i)=x(1,1)+x(2,1)*t(i);
end
plot(t,y_1);

%--------------prediction-----------------%

N_2018=exp(x(1,1)+x(2,1)*2018);
N_2019=exp(x(1,1)+x(2,1)*2019);











